package day02_print_statements;

/*
Q2- Print Triangle
    Create a class called MyTriangle
    Create a main method
    Print below square on the screen:
          *
         * *
        *****
 */
public class MyTriangle {
    public static void main(String[] args) {
        System.out.println("MY TRIANGLE");
        System.out.println("============");
        System.out.println();
        System.out.println("  *");
        System.out.println(" * *");
        System.out.println("*****");
        System.out.println();
    }
}
