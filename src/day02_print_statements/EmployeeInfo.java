package day02_print_statements;
/*
Q4- EmployeeInfo
    Create a class called EmployeeInfo
    Create a main method
    Print:
        Company name:    Google
        Employee:        Khalid
        ID:              12345
        Job Title:       SDET
 */
public class EmployeeInfo {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("Company name:\tGoogle");
        System.out.println("Employee:\t\tKhalid");
        System.out.println("ID:\t\t\t\t12345");
        System.out.println("Job Title:\t\tSDET");
    }
}
