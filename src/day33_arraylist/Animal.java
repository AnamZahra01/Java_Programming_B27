package day33_arraylist;

public class Animal {

    // instance variables
    String species;
    int numberOfLegs;
    int age;
    boolean canFly;
    double weight;

    public String toString() {
        return "Animal{" +
                "species='" + species + '\'' +
                ", numberOfLegs=" + numberOfLegs +
                ", age=" + age +
                ", canFly=" + canFly +
                ", weight=" + weight +
                '}';
    }
}
