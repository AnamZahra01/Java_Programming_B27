package day05_arithmetic_operators;

public class MathPractice {
    public static void main(String[] args) {

        System.out.println(4 + 5);
        System.out.println(4 * 5); //int * int --> int

        System.out.println(2 * 5.5); //int * double --> double

        System.out.println(10 / 3); //int / int --> int
        System.out.println(10 / 3.0); //int / double --> double

        System.out.println(5 % 2); //int % int --> int . 1 left over
        System.out.println( 4 % 2); // modulus operator to know even or odd.

        System.out.println(4 % 10);
        System.out.println(345 % 2);


    }
}
